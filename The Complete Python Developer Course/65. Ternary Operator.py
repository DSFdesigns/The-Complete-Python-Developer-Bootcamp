# 66. Ternary Operator or Conditional Expression (shortcut to if else)(for True or False conditions)

# condition_if_true if condition else condition_if_else
is_friend = False
can_message = "message allowed" if is_friend else "not allowed to message"
print(can_message)

is_chubby = True
on_diet = "cannot eat lots of food" if is_chubby else "can eat lots of food"
print(on_diet)
